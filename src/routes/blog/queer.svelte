<Article
	title="Essere queer al Majorana"
	subtitle="Quae scripsistis, ea timeo."
	authors={['AE', 'TG', 'IR']}
	date={[2022, 3, 23]}
>
	<p>
		Era quasi un mese fa, quando abbiamo fatto un minisondaggio per sapere di come si trovano le
		persone trans al Majo. Era quasi un mese fa, quando abbiamo ricevuto molte più risposte di
		quante potessimo mai aspettarci. Era quasi un mese fa, quando abbiamo scoperto che la situazione
		è molto più tragica di quello che credessimo. Erano quasi due settimane fa quando, sconcertati,
		ci siamo sentiti in dovere di fare un breve post su Instagram per commentare quello che abbiamo
		ricevuto. Erano quasi due settimane fa quando le risposte sono diventate quasi il doppio. Erano
		quasi due settimane fa quando siamo stati ringraziati da più persone per quel post.
	</p>
	<p>
		Ma ora, è difficile pensare. È difficile crederci, ancora di più di prima. Per scrivere questo
		articolo abbiamo riletto tutte le risposte. La situazione è ancora peggiore di quanto avevamo
		pensato originariamente. Ciò detto, penso sia importante dire che non c'è rancore, né da parte
		nostra, né da quella di chi ci ha scritto. Cito (ancora) una frase che ci è stata mandata, forse
		una delle prime: "C'è tanta ignoranza, e l'ignoranza fa male. Fa male a noi, che la subiamo, che
		ci ferisce. Ma fa male anche a loro, ai transfobici accidentali. Non hanno colpe. Sono solo
		stati cresciuti così."
	</p>
	<p>
		Quando ho chiesto agli studenti di parlarmi del Majorana, prima di venire, mi è stato descritto
		come un ambiente inclusivo. Ed effettivamente lo sto vivendo come un ambiente inclusivo. Ma
		chiaramente per molti non è così. Anche la semplice tolleranza, per molti, è un sogno. Una delle
		prime risposte è di un'ex-studentessa, che ha cambiato scuola tre mesi dopo l'inizio del suo
		primo anno scolastico. Cita come ragione principale l'essere bullizzata "solo perché volevo che
		i [suoi] compagni non [la] riempissero inutilmente di disforia" (per chi non lo sapesse, in
		contesto queer "disforia" si riferisce di solito alla disforia di genere, letteralmente lo
		sconforto conseguente ad un'incongruenza tra la propria identità di genere e il proprio sesso
		biologico alla nascita) chiedendo che fosse usato il femminile. Qualcuno ci ha detto che,
		nonostante il bullismo, la violenza, gli insulti, è rimasto in questa scuola sperando che
		succedesse qualcosa che facesse cambiare idea ai perpetratori. Diverse persone ci hanno
		raccontato come le persone che credevano amiche le hanno rifiutate, sapendo del genere
		dell'amico. Una ragazza ha deciso di provare a rimanere vicina alla sua migliore amica,
		nonostante questa la schifasse, e ha nascosto la sua identità a tutta la scuola. Ora ne parla
		solo online, dove i compagni non sanno che è lei.
	</p>
	<p>
		Abbiamo ricevuto anche i messaggi di quelli che in inglese si chiamano allies, gli "alleati",
		insomma chi non è direttamente queer ma supporta la causa (o semplicemente ne è indifferente,
		che spesso basta). Messaggi che denunciano, con molta più freddezza di quelli vissuti
		direttamente, di aver assistito a molteplici episodi di violenza, di rifiuto dei compagni che
		non sono cishet. Messaggi che ammettono di non aver mai avuto il coraggio di denunciare.
		Messaggi che chiedono un aiuto per segnalare. Messaggi di offerta di sostegno, di supporto.
	</p>
	<p>
		Forse per nostra mancanza, non abbiamo considerato di chiedere le storie di chi è cis, ma non
		etero. Abbiamo comunque ricevuto messaggi da parte loro, (grazie ragazzi che avete provveduto a
		colmare la nostra lacuna) altrettanto terrificanti. Le modalità sono pressoché le stesse, e in
		fondo anche le ragioni, dalla transfobia all'omofobia non passa tanto.
	</p>
	<p>
		Potrei andare avanti scrivendo un paragrafo per ogni messaggio ricevuto, ma diventerebbe o un
		video di reaction in formato testuale, o una grande ripetizione degli stessi concetti. La prima
		domanda che ci è venuta è, banalmente, cosa si può fare? Banale è solo la domanda, quasi
		conseguenza degli orrori letti, ma la risposta è tutt'altro che semplice. Non possiamo certo
		pretendere che la tolleranza sia innata. L'istinto animale ci porta a temere il diverso. Ma la
		tolleranza va cresciuta, va coltivata, va insegnata. Non pensiamo sia troppo tardi. Forse non è
		mai troppo tardi, ma decisamente le scuole superiori non sono tardi. La tolleranza non serve
		solo a scuola, la tolleranza serve nella vita. L'educazione alla tolleranza è educazione civica.
		Sarebbe stato meglio cominciare prima, certo, ma siamo ancora in tempo. Educare alla tolleranza
		non sradica certo il seme della queerfobia, ma può aiutare a ridurre il numero di seguaci della
		malsana idea che il diverso è pericoloso in principio. È un sogno, perché non si può non
		definirlo che sogno, ma è anche una speranza, è anche un invito, è soprattutto una necessità.
		Per una scuola più tollerante, più inclusiva (che alla fine significa che non supporta
		silenziosamente le violenze per la propria identità di genere, romantica o sessuale).
		All'articolo 1, comma 2 dello Statuto delle Studentesse e degli Studenti della Scuola Secondaria
		(DPR 249 1998), si dice che "La vita della comunità scolastica si basa sulla libertà di
		espressione, di pensiero, di coscienza e di religione, sul rispetto reciproco di tutte le
		persone che la compongono, quale che sia la loro età e condizione, nel ripudio di ogni barriera
		ideologica, sociale e culturale." Nei doveri troviamo "Gli studenti condividono la
		responsabilità di rendere accogliente l'ambiente scolastico e averne cura come importante
		fattore di qualità della vita della scuola."
	</p>
	<p>
		Per concludere, un po' di dati. Abbiamo ricevuto 90 risposte. Di queste, 68 persone non si
		definiscono cis. 39 si definiscono uomini, 47 donne, 2 persone non binarie, 2 preferiscono non
		rispondere. 30 persone si definiscono non etero. 76 persone dichiarano di aver subito episodi di
		bullismo, violenza o molestie, collegate alla propria identità. 11 persone dicono di essersi
		trovate bene nella propria classe. 27 dicono che almeno uno studente nella propria classe li ha
		sostenuti nella loro ricerca di un'identità. In 22 non hanno mai detto alla propria classe di
		non essere cishet, in 48 non hanno detto di non essere cis ai professori. 87 persone chiedono
		che la scuola faccia qualcosa per ridurre gli episodi di rifiuto di chi è diverso. Le altre 3
		preferiscono non rispondere.
	</p>
</Article>
